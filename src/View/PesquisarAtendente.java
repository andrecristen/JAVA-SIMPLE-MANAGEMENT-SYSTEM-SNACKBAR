    private void selecinaAtendenteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecinaAtendenteButtonActionPerformed
        //Seleciona a linha da tabela que é o atendente selecionado.
        int row = atendentesTable.getSelectedRow();
        if (row >= 0) {
            DefaultTableModel clientesTable = (DefaultTableModel) this.atendentesTable.getModel();
            String codigo = clientesTable.getValueAt(row, 0).toString();
            String funcao = clientesTable.getValueAt(row, 1).toString();
            String nome = clientesTable.getValueAt(row, 2).toString();
            this.retornaValor= codigo + "-" + nome;
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha para prosseguir com a selecão de Atendente.");
        }
    }//GEN-LAST:event_selecinaAtendenteButtonActionPerformed

    private void preencheComFornecedoresCadastrados() {
        AtendenteController controller = new AtendenteController();
        ArrayList<Atendente> data = controller.selecionarTodosAtendentes();
        DefaultTableModel tabela = (DefaultTableModel) atendentesTable.getModel();
        for (Atendente atendente : data) {
            tabela.addRow(new Object[]{atendente.getCodigo().toString(), atendente.getFuncao().toString(), atendente.getNome().toString()+" "+ atendente.getSobrenome().toString()});
        }

    }
