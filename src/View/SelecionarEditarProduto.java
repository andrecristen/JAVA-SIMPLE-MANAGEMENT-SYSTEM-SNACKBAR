    private void selecionarExcluirAtendenteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecionarExcluirAtendenteButtonActionPerformed
        int row = produtoTable.getSelectedRow();
        if (row >= 0) {
            int reply = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja realmente excluir este registro?", "Atenção!", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                DefaultTableModel produtoTable = (DefaultTableModel) this.produtoTable.getModel();
                String codigo = produtoTable.getValueAt(row, 0).toString();
                ProdutoController controller = new ProdutoController();
                controller.removerProdutoByCodigo(codigo);
                while (produtoTable.getRowCount() > 0) {
                    produtoTable.removeRow(0);
                }
                preencheComProdutosCadastrados();
            }
        } else if (row < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para prosseguir com a exclusão de Fornecedor.");
        }
    }//GEN-LAST:event_selecionarExcluirAtendenteButtonActionPerformed

    private void selecionarEditarAtendenteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecionarEditarAtendenteButtonActionPerformed
        ProdutoController controller = new ProdutoController();
        int row = produtoTable.getSelectedRow();
        String codigo = produtoTable.getValueAt(row, 0).toString();
        Produto produto = controller.findProdutoByCodigo(codigo);
        EditarProduto edit = new EditarProduto(null, rootPaneCheckingEnabled, produto);
        edit.setVisible(true);
        DefaultTableModel produtoTable = (DefaultTableModel) this.produtoTable.getModel();
        while (produtoTable.getRowCount() > 0) {
            produtoTable.removeRow(0);
        }
        preencheComProdutosCadastrados();
    }//GEN-LAST:event_selecionarEditarAtendenteButtonActionPerformed

