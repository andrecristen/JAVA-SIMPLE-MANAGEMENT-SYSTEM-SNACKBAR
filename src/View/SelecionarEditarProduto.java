    private void preencheComProdutosCadastrados() {
        ProdutoController controller = new ProdutoController();
        ArrayList<Produto> data = controller.selecionarTodosProdutos();
        DefaultTableModel tabela = (DefaultTableModel) produtoTable.getModel();
        for (Produto produto : data) {
            tabela.addRow(new Object[]{produto.getCodigo().toString(), produto.getNome().toString()});
        }

    }

