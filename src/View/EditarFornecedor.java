    private void loadModelToView(Fornecedor fornecedor) {
        this.codigoFornecedorInput.setText(fornecedor.getCodigo());
        this.nomeFornecedorInput.setText(fornecedor.getNome());
        this.loginFornecedorInput.setText(fornecedor.getLogin());
        this.senhaFornecedorInput.setText(fornecedor.getSenha());
        this.cnpjFornecedorInput.setText(fornecedor.getCnpj());
        this.nomeFantasiaFornecedorInput.setText(fornecedor.getNomeFatasia());
        this.inscricaoEstadualFornecedorInput.setText(fornecedor.getInscricaoEstadual());
        
        DefaultTableModel tabelaEndereco = (DefaultTableModel) tableEnderecoFornecedor.getModel();
        DefaultTableModel tabelaContato = (DefaultTableModel) tableContatosFornecedor.getModel();
        ArquivoController controller = new ArquivoController();
        
        ArrayList<Endereco> enderecos = controller.getEnderecosByCodigoPai(cons.getPATH_PART_INITIAL()+cons.getENDERECO_FORNECEDOR(), fornecedor.getCodigo(), cons.getTIPO_FORNECEDOR());
        for(Endereco endereco : enderecos){
            tabelaEndereco.addRow(new Object[]{endereco.getCodigo(), endereco.getRua(), endereco.getBairro(), endereco.getCidade(), endereco.getEstado(), endereco.getComplemento(), endereco.getReferencia()});
        }
        ArrayList<Contato> contatos = controller.getContatosByCodigoPai(cons.getPATH_PART_INITIAL()+cons.getCONTATO_FORNECEDOR(), fornecedor.getCodigo(), cons.getTIPO_FORNECEDOR());
        for(Contato contato : contatos){
            tabelaContato.addRow(new Object[]{contato.getCodigo(), contato.getTipo(), contato.getConteudo()});
        }
    }

