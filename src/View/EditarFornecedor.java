    private void removeContatoFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeContatoFornecedorActionPerformed
        DefaultTableModel model = (DefaultTableModel) tableContatosFornecedor.getModel();
        try {
            int SelectedRowIndex = tableContatosFornecedor.getSelectedRow();
            int SelectedsRows = tableContatosFornecedor.getSelectedRowCount();
            if (SelectedsRows == 1) {
                model.removeRow(SelectedRowIndex);
            } else if (SelectedsRows > 1) {
                JOptionPane.showMessageDialog(null, "Para garantir uma exclusão mais segura, selecione apenas um linha por vez");
            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma linha para exclusão");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_removeContatoFornecedorActionPerformed

