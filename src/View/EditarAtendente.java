    private void loadModelToView(Atendente atendente) {
        this.codigoAtendenteInput.setText(atendente.getCodigo());
        this.nomeAtendenteInput.setText(atendente.getNome());
        this.sobrenomeAtendenteInput.setText(atendente.getSobrenome());
        this.loginAtendenteInput.setText(atendente.getLogin());
        this.senhaAtendenteInput.setText(atendente.getSenha());
        this.funcaoAtendenteInput.setText(atendente.getFuncao());
        this.nascimentoAtendenteInput.setText(atendente.getDataNascimento());
        this.admissaoAtendenteInput.setText(atendente.getDataAdmissao());
        
        DefaultTableModel tabelaEndereco = (DefaultTableModel) tableEnderecosAtendente.getModel();
        DefaultTableModel tabelaContato = (DefaultTableModel) tableContatosAtendente.getModel();
        ArquivoController controller = new ArquivoController();
        
        ArrayList<Endereco> enderecos = controller.getEnderecosByCodigoPai(cons.getPATH_PART_INITIAL()+cons.getENDERECO_ATENDENTE(), atendente.getCodigo(), cons.getTIPO_ATENDENTE());
        for(Endereco endereco : enderecos){
            tabelaEndereco.addRow(new Object[]{endereco.getCodigo(), endereco.getRua(), endereco.getBairro(), endereco.getCidade(), endereco.getEstado(), endereco.getComplemento(), endereco.getReferencia()});
        }
        ArrayList<Contato> contatos = controller.getContatosByCodigoPai(cons.getPATH_PART_INITIAL()+cons.getCONTATO_ATENDENTE(), atendente.getCodigo(), cons.getTIPO_ATENDENTE());
        for(Contato contato : contatos){
            tabelaContato.addRow(new Object[]{contato.getCodigo(), contato.getTipo(), contato.getConteudo()});
        }
    }
    

