    private void removeContatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeContatoActionPerformed
        DefaultTableModel model = (DefaultTableModel) tableContatosAtendente.getModel();
        try {
            int SelectedRowIndex = tableContatosAtendente.getSelectedRow();
            int SelectedsRows = tableContatosAtendente.getSelectedRowCount();
            if (SelectedsRows == 1) {
                model.removeRow(SelectedRowIndex);
            } else if (SelectedsRows > 1) {
                JOptionPane.showMessageDialog(null, "Para garantir uma exclusão mais segura, selecione apenas um linha por vez");
            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma linha para exclusão");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_removeContatoActionPerformed

