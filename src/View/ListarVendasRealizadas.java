    private void preencheComVendasRealizadas(){
        VendaController controller = new VendaController();
        ArrayList<Venda> data = controller.selecionarTodasVendas();
        DefaultTableModel tabela = (DefaultTableModel) listVendasTable.getModel();
        for (Venda venda : data) {
            String atendenteNome = venda.getAtendente().getNome()+" "+venda.getAtendente().getSobrenome();
            String clienteNome = venda.getCliente().getNome()+" "+venda.getCliente().getSobrenome();
            if(clienteNome.contains("null")){
                clienteNome = " ";
            }
            tabela.addRow(new Object[]{venda.getCodigo(), atendenteNome, clienteNome, venda.getDataCompra()});
        }
    }
